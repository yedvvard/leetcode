class Solution {
public:
    ListNode* reverseList(ListNode* head) {
        ListNode* pre = nullptr;
        ListNode* curr = head;
        ListNode* tmp = nullptr;

        while(curr){
            tmp = curr->next;
            curr->next = pre;
            pre = curr;
            curr = tmp;
        }
    return pre;
    }
};
